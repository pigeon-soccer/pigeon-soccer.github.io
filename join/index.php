        <div class="whyVolunteerSectionTitle section-title">
          <span>ボランティアの価値</span>
          <h2>ボランティアの価値</h2>
          <p></p>
        </div>
        <div class="row">
          <blockquote class="blockquote blockquoteDecoration">
            <p>一緒にいてくれてありがとうございます。<br>
            外であんないい顔の我が子を初めて見ました、奇跡です。</p>
            <cite>
              保護者様より
            </cite>
          </blockquote>
          <p>
            このページに辿りついていただき、ありがとうございます。
            <br>あなたがボランティアを募集するこのページに辿りついたのは、なぜでしょうか？
            <br>
            <br>社会の役に立ちたいからでしょうか？
            <br>それとも、自らの成長のためでしょうか？
            <br>または、大学における福祉の講義の一環でしょうか？
          </p>
          <hr>
          <p>
            はじめまして、「NPO法人 ピジョン」の運営に携わっている江俣と申します。
            <br>この教室の目的は、チームスポーツであるサッカーを通して、
            <br><span class="enrichText">障がいのある子どもが笑顔になる居場所、学ぶ場としてあり続けること</span>です。
          </p>
          <figure class="figure">
            <img loading="lazy" width=602px height=453px src="../assets/img/snapshot/staff-rina-and-student-girl-art.jpg" class="figure-img img-fluid rounded" alt="...">
