          <div class="commonInner sectionContent commonInner-solution">
            <h2 id="solution">3つの取り組み</h2>
            <p>私たちピジョンは、以下の方法で課題解決に取り組んでいきます。</p>
            <ul class="commonInner__solution">
              <li class="solution__list">
                <dl>
                  <h3>
                    <dt>1. 子どもと保護者のコミュニティと成長の場の提供</dt>
                  </h3>
                  <dd>- 継続的な活動を通じて、障がいのある子どもたち、および保護者たちが仲間とつながり、笑顔になり、安心して過ごせるコミュニティを提供します。</dd>
                  <dd>- サッカーを通じて、社会性や自己管理能力を養育し、将来の自立と就労につなげます。</dd>
                </dl>
              </li>
              <li class="solution__list">
                <dl>
                  <h3>
                    <dt>2. 保護者・学校との連携による支援体制</dt>
                  </h3>
                  <dd>- 保護者や学校関係者と協力し、障がいのあるお子さまの特性に合わせた支援体制を築きます。</dd>
                  <dd>- 教育の専門家とも協力し、お子さまの可能性を最大限に引き出します。</dd>
                </dl>
              </li>
              <li class="solution__list">
                <dl>
                  <h3>
                    <dt>3. 社会との交流と障がいへの理解促進</dt>
                  </h3>
                  <dd>練習や地域活動（またはオンライン発信）を通じて、健常者の障がいへの理解を促します。</dd>
                  <dd>健常者が交流できる機会を作り、身近な体験から偏見と無理解を解消します。</dd>
                </dl>
              </li>
            </ul>
