          <div class="commonInner sectionContent commonInner-problem">
            <h2 id="problem">3つの課題感</h2>
            <p>私たち「ピジョン」は活動を始めてから、３つの課題感を持ちました。</p>
            <section class="subSummary subSummary-connection">
              <h3>１．居場所と経験の不足</h3>
              <div class="paragraph">
                <input id="paragraph-01" type="checkbox">
                <label for="paragraph-01"></label>
                <div class="paragraph-content">
                  <h4>「本当は、サッカーじゃなくても良かったんです」</h4>
                  <p>
                    ピジョン設立から一年が経った頃、ある生徒のお母さんがこう語りました。
                    障がいのある子どもの習い事としては、水泳や太鼓などの個人競技が一般的です。
                    しかし、彼女はお子さんが他者と関わりながら成長することを願い、地域のクラブチームを探していました。
                    ところが、野球でもテニスでも、ダウン症などの知的障がい、自閉症や多動症（ADHD）などの発達障がい、および脳性まひなどの身体障がいのある子どもを受け入れるチームはなかなか見つかりません。
                    サッカーチームも数多く存在するものの、そのようなチームは当時ほとんどありませんでした。
                    つまり、スポーツ種目を選ぶ選択肢がなく、障がいのある子どもたちが経験を積む機会が圧倒的に不足していたのです。
                  </p>
                  <p>
                    さらに、行政が障がいのある子ども向けにイベントを開催することもありますが、それらは年に1、2回程度に過ぎません。
                    中には、時間をかけなければ物事が定着しない子どももいます。
                    そのため、継続的に経験を積む機会や、安心して過ごせる居場所がもっと必要なのです。
                  </p>
                  <h4>「居場所があるだけでも十分なんです」</h4>
                  <p>
                    親御さんは、お子さんを体験会に連れてきたときのエピソードを語ってくれました。
                    「普段、（うちの子は）大勢の人の中に入ることができず、すぐに帰りたがります。
                    そして『もう行きたくない』と言って、行かなくなってしまいます。
                    でも、ここでは『たまたま』居ることができました。
                    体験のときはただ佇んでいただけですが、居られる場所があるだけでも十分だと思いました。」
                    この話を聞き、私たちは知的障がいや発達障がいのある子どもたちが安心して過ごせる居場所がいかに限られているかを痛感しました。
                  </p>
                </div>
              </div>
            </section>
            <section class="subSummary subSummary-connection">
              <h3>２．自立と就職への道のり</h3>
              <div class="paragraph">
                <input id="paragraph-02" type="checkbox">
                <label for="paragraph-02"></label>
                <div class="paragraph-content">
                  <h4>「私たちがいなくなったら——」</h4>
                  <p>
                    彼女はさらに話を続けました。
                    「その後、何度か活動に参加するうちに、息子は少しずつボールに触れ、人とも関わるようになりました。次第に彼は私に『サッカーはいつ？』と尋ねたり、『サッカーをしに行くんだよ』と嬉しそうに先生に伝えるようにもなりました。本当に感謝しています。
                    でも、もう一つ、無理だとわかっていますがお願いがあります。
                    私たちがいる間はこの子をサポートできます。
                    しかし、私たちがいなくなったら、この子を支えてくれる人がいなくなってしまいます。
                    だから、この子が自立できるようにしてほしいのです。」
                  </p>
                  <h4>「障がいを持つ人の離職率は30〜50％」</h4>
                  <p>
                    親にとって、子どもの自立は切実な課題です。しかし残念ながら、知的障がいや発達障がいのある子どもが大人になり、せっかく就職しても「人間関係がうまくいかない」「働く意欲や体力が続かない」「作業が合わない・適応できない」などの理由で、すぐに離職してしまうケースが多々あります。
                    実際に厚生労働省の調査（※）によれば、就職から1年以内の離職率が全国平均で15％であるのに対し、障がいを持つ人たちの離職率は30〜50％と高い数字となっています。
                    そのため、子どものころから社会性や自己管理能力を養育し、自立を促す機会が必要であると考えています。
                  </p>
                  <hr commonLine-problem>
                  <a href="https://www.mhlw.go.jp/file/05-Shingikai-11601000-Shokugyouanteikyoku-Soumuka/0000178930.pdf"
                    target="_blank">※参考『障害者雇用の現状等 平成２９年９月２０日 厚生労働省職業安定局』</a>
                </div>
              </div>
            </section>
            <section class="subSummary subSummary-connection">
              <h3>３．偏見と諦め</h3>
              <div class="paragraph">
                <input id="paragraph-03" type="checkbox">
                <label for="paragraph-03"></label>
                <div class="paragraph-content">
                  <h4>「レッテル」</h4>
                  <p>
                    人は一人では生きていくことはできません。
                    しかし、多くの健康な子どもたちは、ダウン症などの知的障がい、自閉症や多動症（ADHD）などの発達障がい、および脳性まひなどの身体障がいのある子どもたちと接する機会がほとんどないまま、社会に出ていきます。
                    そのため、社会全体が障がいを持つ人々への無理解や偏見を持ちやすい構造になっています。
                    しかし、私たちピジョンは、彼ら彼女らの純粋さや可能性を日々感じています。
                    スポーツを通じて人と関わり、自己管理を習慣化することで、障がいのある子どもたちも少しずつ心身ともに成長していく—これは私たちが直接目にしてきた事実です。
                    この事実が広く認知されることで、障がいの有無にかかわらず、子どもたちが互いに尊重し合い、共に成長できる社会が実現すると信じています。
                    保護者の皆さまも、諦めずにお子さまの可能性を信じていただきたいと思います。
                  </p>
                </div>
              </div>
            </section>
